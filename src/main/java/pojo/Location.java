package pojo;

public class Location {
     double lat;
     double lang;

     public double getLat() {
        return lat;
    }
    public void setLat(double lat) {
        this.lat = lat;
    }
    public double getLang() {
        return lang;
    }
    public void setLang(double lang) {
        this.lang = lang;
    }
}
